export const generateTideRecords = (): TideRecord[] => {
  const records: TideRecord[] = [];
  const baseHeight = 4.5;
  const amplitude = 2.5;
  const start = new Date(now.getTime() - 24 * 3600 * 1000);
  for (let i = 0; i < 96; i++) {
    const t = new Date(start.getTime() + i * 30 * 60 * 1000);
    const phase = (i / 96) * Math.PI * 4;
    const height = baseHeight + amplitude * Math.sin(phase);
    let type: TideRecord['type'] = 'rising';
    const nextPhase = ((i + 1) / 96) * Math.PI * 4;
    const nextHeight = baseHeight + amplitude * Math.sin(nextPhase);
    if (height > baseHeight + amplitude * 0.95) type = 'high';
    else if (height < baseHeight - amplitude * 0.95) type = 'low';
    else if (nextHeight > height) type = 'rising';
    else type = 'falling';
    records.push({ time: t, height: Math.max(1, Number(height.toFixed(2))), type });
  }
  return records;
};

